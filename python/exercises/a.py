"""
Ler dois números inteiros, executar e mostrar o resultado das 
seguintes operações: adição, subtração, multiplicação e divisão
"""

num_1 = int(input('Digite o primeiro número: '))
num_2 = int(input('Digite o segundo número: '))

# Adição
print(num_1 + num_2)

# Subtração
print(num_1 - num_2)

# Multiplicação
print(num_1 * num_2)

# Divisão
print(num_1 / num_2)
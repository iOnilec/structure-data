# Manipulação de váriaveis e constantes

# int
number_int_1 = 23
number_int_2 = -90

# Printar váriaveis separadamente
print(number_int_1)
print(number_int_2)

# Printar váriaveis concatenadas
print(number_int_1, number_int_2, 'Oi, Mundo.')

# Strings e chars
word_str_1 = 'linguagem de programação'
word_str_2 = 'chamada Python'

print('Estou aprendendo uma', word_str_1, word_str_2)

# Input (entrada do usuário)
age = input('Qual a sua idade? ')

print('Você tem', age, 'anos de idade')

name = input('Qual é o seu nome? ')

print('Seu nome é', name)

# Adicionar a tipagem no input()
age_type = int(input('Qual a sua idade? '))

print('Sua idade é', age_type)

name_type = str(input('Qual é o seu nome? '))

print('Seu nome é', name_type)
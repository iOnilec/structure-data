# Operadores lógicos

a = True
b = False

# São iguais?
print(a, b)
print(a and b)
print(a & b)
print("'A' e 'B' são iguais é", a & b)

# Ou são iguais?
print(a or b)
print(a | b)
print("'A' e 'B' são iguais é", a | b)

# Não é True ou False?
print(not a)
print(not b)


# Operadores relacionais

c = 45
d = 46

# C é maior que D?
print(c > d)
# C é igual a D?
print(c == d)
# C é maior ou igual a D?
print(c >= d)
# C é menor que D?
print(c < d)
# C é menor ou igual a D?
print(c <= d)
# C é diferente de D?
print(c != d)